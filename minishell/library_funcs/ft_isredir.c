#include "libft.h"

int	ft_isredir(char *chr)
{
	if (chr[0] == '>')
	{
		if (chr[1] == '>')
			return (2);
		return (1);
	}
	if (chr[0] == '<')
	{
		if (chr[1] == '<')
			return (2);
		return (1);
	}
	return (0);
}
